    private void txtMontoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMontoKeyReleased
        if(!"".equals(txtMonto.getText())){
            if(!txtMonto.getText().endsWith(".")){
        if(indicador==3){
            double nuevoImporte = precioUnitario * Double.parseDouble(txtMonto.getText());
            txtResiduo.setText("$"+formateador.format(nuevoImporte));
         }else{
             double nuevoImporte =(precioUnitario*cantidad)-Double.parseDouble(txtMonto.getText()); 
            txtResiduo.setText("$"+formateador.format(nuevoImporte));
         } 
        }else{
         txtResiduo.setText("");
        }
        }else{
         txtResiduo.setText("");
        }
        
        int codigo=evt.getKeyCode();
        switch(codigo){
            case KeyEvent.VK_ENTER:
            eventoEnter();
            break;

            case KeyEvent.VK_ESCAPE:
            dispose();
            break;

        }
    }//GEN-LAST:event_txtMontoKeyReleased

