    public void vaciarProducto(int indicador, double cantidad, double precioUnitario, String codigo){
        this.indicador=indicador;
        this.cantidad=cantidad;
        this.precioUnitario=precioUnitario;
        this.codigo=codigo;
        p = productoDao.BuscarPorCodigo(codigo);
        txtPrecioUnitario.setText("$"+formateador.format(precioUnitario));
        txtPiezas.setText(formato.format(cantidad));
        txtImporte.setText("$"+formateador.format(precioUnitario*cantidad));
        if(indicador==1){ //descuento
           txtTitulo.setText("Descuento por articulo");
           this.setTitle("Punto de venta - Descuento por articulo");
           txtResiduo.setToolTipText("Saldo final");
          txtMonto.setToolTipText("Cantidad a descontar");
        } 
        if(indicador==2){ //bonificacion
           txtTitulo.setText("Bonificación por articulo");
           this.setTitle("Punto de venta - Bonificación por articulo");
             txtResiduo.setToolTipText("Saldo final");
             txtMonto.setToolTipText("Cantidad a bonificar");
        } 
        if(indicador==3){ //devolucion
           txtTitulo.setText("Cantidad de piezas");
           this.setTitle("Punto de venta - Cantidad de piezas");
           lbLimpio.setText("Piezas");
          txtMonto.setToolTipText("Cantidad de piezas a regresar");
           lbCantidad.setText("Subtotal");
           txtResiduo.setToolTipText("Importe por piezas devueltas");
        } 
    }
    

    
    public void eventoEnter(){
         if(!"".equals(txtMonto.getText())){
            if(!txtMonto.getText().endsWith(".")){
                cantidadModificada = Double.parseDouble(txtMonto.getText());
                if(indicador==3){
                     if(Double.parseDouble(txtMonto.getText())>cantidad){
                         Notification panel = new Notification(new javax.swing.JFrame(), Notification.Type.WARNING, Notification.Location.TOP_CENTER, "Cantidad no valida");
                                 panel.showNotification();   
                     }else{
                         accionRealizada=true;
                         dispose();
                     }
                  }else{
                    if(Double.parseDouble(txtMonto.getText())>(precioUnitario*cantidad)){
                     Notification panel = new Notification(new javax.swing.JFrame(), Notification.Type.WARNING, Notification.Location.TOP_CENTER, "Cantidad no valida");
                                 panel.showNotification();   
                    }else{
                         accionRealizada=true;
                         dispose();
                    }
                }         
            }else{
            Notification panel = new Notification(new javax.swing.JFrame(), Notification.Type.WARNING, Notification.Location.TOP_CENTER, "No se ha ingresado ninguna cantidad");
                                 panel.showNotification();
        }
         }else{
            Notification panel = new Notification(new javax.swing.JFrame(), Notification.Type.WARNING, Notification.Location.TOP_CENTER, "No se ha ingresado ninguna cantidad");
                                 panel.showNotification();
        }
        
      
    }
    
    

